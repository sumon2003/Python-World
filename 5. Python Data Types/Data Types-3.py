"""
Setting the Data Type::>
In Python, the data type is set when you assign a value to a variable.

"""

#6. Data Type 'tuple':
x = ("apple", "banana", "cherry")

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#7. Data Type 'range':
x = range(6)

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#8. Data Type 'dict':
x = {"name" : "Musafir", "age" : 20}

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#9. Data Type 'set':
x = {"apple", "banana", "cherry"}

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#10. Data Type 'frozenset':
x = frozenset({"apple", "banana", "cherry"})

#display x:
print(x)

#display the data type of x:
print(type(x)) 
