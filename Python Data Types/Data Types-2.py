"""
Setting the Data Type::>
In Python, the data type is set when you assign a value to a variable.

"""

#1. Data Type 'str':
x = "Python World"

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#2. Data Type 'int':
x = 20

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#3. Data Type 'float':
x = 20.5

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#4. Data Type 'complex':
x = 1j

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#5. Data Type 'list':
x = ["apple", "banana", "cherry"]

#display x:
print(x)

#display the data type of x:
print(type(x)) 



