"""
Setting the Data Type::>
In Python, the data type is set when you assign a value to a variable.

"""

#11. Data Type 'bool':
x = True

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#12. Data Types 'bytes':
x = b"Hello"

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#13. Data Type 'bytearray':
x = bytearray(5)

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#14. Data Type 'memoryview':
x = memoryview(bytes(5))

#display x:
print(x)

#display the data type of x:
print(type(x)) 

#=================================================

#15. Data Type 'NoneType':
x = None

#display x:
print(x)

#display the data type of x:
print(type(x)) 





